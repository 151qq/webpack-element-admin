<template>
  <section>
    <banner-house></banner-house>

    <section class="card-list">

      <el-button class="set-btn" type="primary" size="mini" @click="showModel">
        设置
      </el-button>

      <div class="input-box">

        <search-box></search-box>
        
        <router-link class="map-btn" :to="{ name: 'map', params: { type: type }, query: {city: activeName}}">
          <img src="../../assets/images/map-icon.png">
          地图
        </router-link>
      </div>

      <el-tabs class="card-box" v-model="activeName">
        <el-tab-pane 
            class="card-outer"
            v-for="item in selectedCity"
            :label="item"
            :name="item">
          <el-row>
            <el-col 
                :class="index % 4 == 0 ? 'card-b clearM' : 'card-b'"
                :span="6"
                v-for="(o, index) in dataCity[citys.indexOf(item)].articles">
              <router-link class="linkA" target="_blank" :to="{ name: 'detail', params: { id: o.id }}">
                <el-card :body-style="{ padding: '0px' }">
                  <img :src="o.imgUrl" class="image">
                  <div style="padding: 14px;">
                    <span>{{ o.title }}</span>
                    <div class="bottom clearfix">
                      <time class="time">{{ o.date }}</time>
                    </div>
                  </div>
                </el-card>
              </router-link>
            </el-col>
          </el-row>
        </el-tab-pane>
      </el-tabs>
    </section>
  
    <address-form 
        :citys="citys" 
        :check-list="selectedCity"
        :dialog-form-visible="dialogFormVisible"
        @sendD="changeD">
    </address-form>

  </section>
</template>
<script>
import bannerHouse from '../../components/views/banner-house.vue'
import searchBox from '../../components/common/search-box.vue'
import addressForm from '../../components/views/address.vue'
import Tools from '../../utils/tools.js'

export default {
  data () {
    return {
      activeName: '',
      citys: [],
      selectedCity: [],
      dataCity: [],
      restaurants: [],
      dialogFormVisible: {
        visibleA: false
      },
      type: '',
      pageInfo: {}
    }
  },
  created () {
    this.setData()
    this.getCitys()
  },
  watch: {
    $route (to, from) {
      this.setData()
      this.getCitys()
    },
    activeName (value) {
      this.pageInfo.city = value
      this.$store.dispatch('setPageInfo', this.pageInfo)
      window._eventObject.$emit('cityChange')
    }
  },
  methods: {
    // 页面初始数据设置
    setData () {
      // 初始tab
      this.activeName = this.selectedCity[0]
      this.type = this.$route.params.type
      this.pageInfo = {
        type: this.type,
        city: this.activeName
      }

      this.$store.dispatch('setPageInfo', this.pageInfo)
    },
    // 获取banner楼盘数据
    getPMess () {
      Tools.getJson('url', this.pageInfo, (datas) => {

      })
    },
    // 获取城市及报告数据
    getCitys () {
      let formData = {
        type: this.pageInfo.type
      }

      Tools.getJson('reports', formData, (res) => {
        if (res.statusCode === 0) {
          this.dataCity = res.datas
          // 设置所有城市
          this.citys = res.datas.map((item) => {
            return item.name
          })
          // 设置当前显示
          this.selectedCity = this.citys.slice(0, 1)
          this.activeName = this.selectedCity[0]
        } else {
          this.$message.error(res.mess)
        }
      })
    },
    // 设置城市弹窗
    showModel () {
      this.dialogFormVisible.visibleA = true
    },
    // 改变城市子组件数据传回
    changeD (data) {
      this.selectedCity = data
    }
  },
  components: {
    bannerHouse,
    addressForm,
    searchBox
  }
}
</script>
